async def _evaluate_step_pair_async(
    generated_step: Path,
    ground_truth_step: Path,
    output_dir: Path,
    threshold: float,
    timeout_seconds: int,
) -> dict[str, Any]:
    generated_step = generated_step.resolve()
    ground_truth_step = ground_truth_step.resolve()
    output_dir = output_dir.resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    code = _build_eval_code(
        generated_step_b64=_to_b64(generated_step),
        ground_truth_step_b64=_to_b64(ground_truth_step),
        threshold=threshold,
        view_specs=DEFAULT_VIEW_SPECS,
    )
    runner = McpSandboxRunner(
        command=settings.sandbox_mcp_server_command,
        args=settings.sandbox_mcp_server_args_list,
        cwd=settings.sandbox_mcp_server_cwd_effective,
        timeout_buffer_seconds=settings.sandbox_mcp_timeout_buffer_seconds,
    )
    result = await runner.execute(code=code, timeout=timeout_seconds, requirement_text=None)

    raw_output_files = list(
        dict.fromkeys([*result.output_files, *result.output_file_contents.keys()])
    )
    output_files = [
        name
        for name in raw_output_files
        if not (name.startswith("preview_") and name.lower().endswith(".png"))
    ]
    for name, content in result.output_file_contents.items():
        if name not in output_files:
            continue
        (output_dir / name).write_bytes(content)

    if not result.success:
        payload = {
            "status": "error",
            "error_message": result.error_message or result.stderr or "evaluation failed",
            "output_files": output_files,
        }
        (output_dir / "benchmark_eval.json").write_text(
            json.dumps(payload, ensure_ascii=True, sort_keys=True, indent=2),
            encoding="utf-8",
        )
        return payload

    raw_eval = result.output_file_contents.get("geometry_info.json")
    if raw_eval is None:
        parsed_stdout = _extract_eval_from_stdout(result.stdout)
        if parsed_stdout is not None:
            payload = parsed_stdout
            payload["output_files"] = output_files
            (output_dir / "benchmark_eval.json").write_text(
                json.dumps(payload, ensure_ascii=True, sort_keys=True, indent=2),
                encoding="utf-8",
            )
            (output_dir / "benchmark_eval_summary.txt").write_text(
                "; ".join(payload.get("difference_notes", [])[:3]),
                encoding="utf-8",
            )
            return payload
        payload = {
            "status": "error",
            "error_message": "geometry_info.json missing from evaluation output",
            "output_files": output_files,
        }
        (output_dir / "benchmark_eval.json").write_text(
            json.dumps(payload, ensure_ascii=True, sort_keys=True, indent=2),
            encoding="utf-8",
        )
        return payload

    try:
        payload = json.loads(raw_eval.decode("utf-8"))
    except Exception as exc:  # noqa: BLE001
        payload = {
            "status": "error",
            "error_message": f"failed to parse evaluation json: {exc}",
            "output_files": output_files,
        }
        (output_dir / "benchmark_eval.json").write_text(
            json.dumps(payload, ensure_ascii=True, sort_keys=True, indent=2),
            encoding="utf-8",
        )
        return payload

    payload["output_files"] = output_files
    (output_dir / "benchmark_eval.json").write_text(
        json.dumps(payload, ensure_ascii=True, sort_keys=True, indent=2),
        encoding="utf-8",
    )
    (output_dir / "benchmark_eval_summary.txt").write_text(
        "; ".join(payload.get("difference_notes", [])[:3]),
        encoding="utf-8",
    )
    return payload


