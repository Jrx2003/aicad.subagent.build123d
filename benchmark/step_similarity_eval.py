            "gen_face_types = set(GEN_FACE_ANCHORS.keys())",
            "gt_face_types = set(GT_FACE_ANCHORS.keys())",
            "gen_plane_only = bool(gen_face_types) and gen_face_types.issubset({'PLANE'})",
            "gt_plane_only = bool(gt_face_types) and gt_face_types.issubset({'PLANE'})",
            "gen_non_planar_faces = sum(float(v) for k, v in GEN_STATS['face_type_hist'].items() if k != 'PLANE')",
